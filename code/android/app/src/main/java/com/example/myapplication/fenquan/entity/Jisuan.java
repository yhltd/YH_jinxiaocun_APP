package com.example.myapplication.fenquan.entity;

public class Jisuan {
    private int id;
    private String thiscolumn;
    private String gongshi;
    private String company;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getThiscolumn() {
        return thiscolumn;
    }

    public void setThiscolumn(String thiscolumn) {
        this.thiscolumn = thiscolumn;
    }

    public String getGongshi() {
        return gongshi;
    }

    public void setGongshi(String gongshi) {
        this.gongshi = gongshi;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }
}
